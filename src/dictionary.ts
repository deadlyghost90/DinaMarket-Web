export function getTranslation(key: keyof typeof translations.en, lang: Language, replacements?: Record<string, string | number>): string {
  let text = translations[lang][key] || translations["en"][key] || "";
  if (replacements) {
    Object.entries(replacements).forEach(([placeholder, val]) => {
      text = text.replace(`{${placeholder}}`, String(val));
    });
  }
  return text;
}
